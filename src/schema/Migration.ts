/**
 * Migration base class — extend this to create database migrations.
 *
 * @implements FR34
 */

import type { AtlasDialect } from "../query/native.js";
import { Schema } from "./SchemaBuilder.js";

/**
 * Base class for all migrations.
 *
 * Usage:
 *   import { Migration } from '@c9up/atlas'
 *
 *   export default class CreateOrders extends Migration {
 *     async up() {
 *       this.schema.createTable('orders', (table) => {
 *         table.id()
 *         table.string('status', 50).notNullable().defaultTo("'pending'")
 *         table.decimal('total', 10, 2).notNullable()
 *         table.timestamps()
 *       })
 *     }
 *
 *     async down() {
 *       this.schema.dropTable('orders')
 *     }
 *   }
 */
export abstract class Migration {
	readonly schema: Schema;
	readonly #dialect: AtlasDialect;

	constructor(dialect?: AtlasDialect) {
		this.schema = new Schema(dialect);
		// Default to sqlite when no dialect is passed — the generator emits
		// sqlite-compatible helpers (`CURRENT_TIMESTAMP`) which happen to work
		// on postgres/mysql too, so this is the safest conservative default.
		this.#dialect = dialect ?? "sqlite";
	}

	/** Apply the migration. */
	abstract up(): Promise<void> | void;

	/** Rollback the migration. */
	abstract down(): Promise<void> | void;

	/** Helper: raw SQL expression (for DEFAULT values). */
	raw(expression: string): string {
		return expression;
	}

	/**
	 * Dialect-aware "current timestamp" helper for column defaults.
	 *
	 * - `sqlite`   → `CURRENT_TIMESTAMP` (the only form SQLite understands)
	 * - `postgres` → `NOW()`
	 * - `mysql`    → `NOW()`
	 *
	 * Previously this always returned `NOW()`, which silently broke SQLite
	 * migrations — `NOW()` is not a recognised function there and the column
	 * default would blow up at DDL compile time.
	 */
	now(): string {
		return this.#dialect === "sqlite" ? "CURRENT_TIMESTAMP" : "NOW()";
	}

	/** Get the SQL statements generated by this migration. */
	async getUpSQL(): Promise<string[]> {
		this.schema.reset();
		await this.up();
		return this.schema.toSQL();
	}

	async getDownSQL(): Promise<string[]> {
		this.schema.reset();
		await this.down();
		return this.schema.toSQL();
	}
}
