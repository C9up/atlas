pub struct ColumnDef {
    pub name: String,
    #[serde(flatten)]
    pub type_spec: ColumnTypeSpec,
    #[serde(default)]
    pub nullable: bool,
    #[serde(default)]
    pub primary: bool,
    /// Dialect-appropriate auto-increment / identity column (Lucid `increments()`):
    /// SQLite `INTEGER PRIMARY KEY AUTOINCREMENT`, Postgres `GENERATED BY DEFAULT
    /// AS IDENTITY`, MySQL `AUTO_INCREMENT`. Implies PRIMARY KEY.
    #[serde(default)]
    pub auto_increment: bool,
    #[serde(default)]
    pub unique: bool,
    /// MySQL `UNSIGNED` numeric modifier (Lucid `unsigned()`). Ignored on
    /// Postgres and SQLite, which have no unsigned integer types.
    #[serde(default)]
    pub unsigned: bool,
    #[serde(default)]
    pub default: Option<String>,
    #[serde(default)]
    pub references: Option<ForeignKeyRef>,
    /// Column comment (Lucid/Knex `comment()`). MySQL renders it inline;
    /// Postgres needs a separate `COMMENT ON COLUMN` statement (emitted by the
    /// caller); SQLite has no column comments and drops it.
    #[serde(default)]
    pub comment: Option<String>,
    /// Column collation (Lucid/Knex `collate()`), e.g. `utf8mb4_unicode_ci`.
    /// Validated as an identifier — it is interpolated, not bound.
    #[serde(default)]
    pub collate: Option<String>,
    /// Position of a newly added column (Lucid/Knex `first()` / `after(col)`).
    /// MySQL-only: Postgres and SQLite always append and reject the clause.
    #[serde(default)]
    pub position: Option<ColumnPosition>,
}

