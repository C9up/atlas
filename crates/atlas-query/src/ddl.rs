/// Render a single column definition (`"name" TYPE [PRIMARY KEY] [NOT NULL] …
/// [REFERENCES …]`) — shared by CREATE TABLE and ALTER TABLE ADD/ALTER COLUMN.
fn render_column_def(col: &ColumnDef, dialect: Dialect) -> Result<String, String> {
    let mut parts: Vec<String> = vec![dialect.quote_ident(&col.name)?];
    if col.auto_increment {
        // Type + PRIMARY KEY + identity are one dialect-specific clause;
        // NOT NULL / UNIQUE / DEFAULT are subsumed by the identity column.
        parts.push(dialect.auto_increment_column(col.type_spec.kind));
    } else {
        if col.type_spec.kind == ColumnTypeKind::Enum {
            // MySQL has a native ENUM(...); pg/sqlite render TEXT + a CHECK that
            // pins the value set (Lucid's non-native enum behaviour).
            let values = col.type_spec.values.as_deref().unwrap_or(&[]);
            if values.is_empty() {
                return Err("E_ENUM_EMPTY: an enum column requires at least one value".into());
            }
            let literals = values
                .iter()
                .map(|v| quote_str_literal(v))
                .collect::<Result<Vec<_>, _>>()?
                .join(", ");
            match dialect {
                Dialect::Mysql => parts.push(format!("ENUM({})", literals)),
                _ => {
                    parts.push("TEXT".into());
                    parts.push(format!(
                        "CHECK ({} IN ({}))",
                        dialect.quote_ident(&col.name)?,
                        literals
                    ));
                }
            }
        } else {
            parts.push(dialect.map_column_type(&col.type_spec)?);
            // MySQL-only UNSIGNED modifier — directly after the numeric type.
            if col.unsigned && matches!(dialect, Dialect::Mysql) {
                parts.push("UNSIGNED".into());
            }
        }
        if col.primary { parts.push("PRIMARY KEY".into()); }
        if !col.nullable { parts.push("NOT NULL".into()); }
        if col.unique { parts.push("UNIQUE".into()); }
        if let Some(default) = &col.default {
            parts.push(dialect.wrap_default(default));
        }
    }
    // COLLATE binds to the type, so it must precede the constraints.
    if let Some(collation) = &col.collate {
        parts.push(format!("COLLATE {}", dialect.quote_ident(collation)?));
    }
    if let Some(fk) = &col.references {
        parts.push(format!(
            "REFERENCES {}({})",
            dialect.quote_ident(&fk.table)?,
            dialect.quote_ident(&fk.column)?
        ));
        if let Some(action) = &fk.on_delete {
            parts.push(format!("ON DELETE {}", referential_action(action)?));
        }
        if let Some(action) = &fk.on_update {
            parts.push(format!("ON UPDATE {}", referential_action(action)?));
        }
    }
    // Only MySQL carries a comment inside the column definition. Postgres needs
    // a separate COMMENT ON COLUMN (see `column_comment_stmts`) and SQLite has
    // no column comments at all.
    if let Some(comment) = &col.comment {
        if dialect == Dialect::Mysql {
            parts.push(format!("COMMENT {}", quote_str_literal(comment)?));
        }
    }
    Ok(parts.join(" "))
}

/// `COMMENT ON COLUMN` statements for a Postgres table — Postgres cannot carry
/// a comment inside the column definition the way MySQL does.
fn column_comment_stmts(
    table: &str,
    columns: &[ColumnDef],
    dialect: Dialect,
) -> Result<Vec<String>, String> {
    if dialect != Dialect::Postgres {
        return Ok(vec![]);
    }
    columns
        .iter()
        .filter_map(|c| c.comment.as_ref().map(|cm| (c, cm)))
        .map(|(c, comment)| {
            Ok(format!(
                "COMMENT ON COLUMN {}.{} IS {};",
                dialect.quote_ident(table)?,
                dialect.quote_ident(&c.name)?,
                quote_str_literal(comment)?
            ))
        })
        .collect()
}

/// The MySQL clause tail (`ENGINE=… DEFAULT CHARSET=… COLLATE=… COMMENT=…`)
/// appended after `CREATE TABLE (...)`. Empty on every other dialect.
fn table_options_clause(options: &TableOptions, dialect: Dialect) -> Result<String, String> {
    if dialect != Dialect::Mysql {
        return Ok(String::new());
    }
    let mut parts: Vec<String> = vec![];
    if let Some(engine) = &options.engine {
        parts.push(format!("ENGINE = {}", dialect.quote_ident(engine)?));
    }
    if let Some(charset) = &options.charset {
        parts.push(format!("DEFAULT CHARSET = {}", dialect.quote_ident(charset)?));
    }
    if let Some(collate) = &options.collate {
        parts.push(format!("COLLATE = {}", dialect.quote_ident(collate)?));
    }
    if let Some(comment) = &options.comment {
        parts.push(format!("COMMENT = {}", quote_str_literal(comment)?));
    }
    Ok(if parts.is_empty() { String::new() } else { format!(" {}", parts.join(" ")) })
}

/// Compile CREATE TABLE → one or more SQL statements (the CREATE itself + CREATE INDEX for each index).
pub fn compile_create_table(spec: &CreateTableSpec, dialect: Dialect) -> Result<Vec<String>, String> {
    if spec.columns.is_empty() {
        return Err("CREATE TABLE requires at least one column".into());
    }
    let table = dialect.quote_ident(&spec.table)?;

    let mut body: Vec<String> = spec.columns.iter()
        .map(|col| Ok(format!("  {}", render_column_def(col, dialect)?)))
        .collect::<Result<Vec<_>, String>>()?;
    // Table-level constraints sit alongside the column lines, after them.
    for c in &spec.constraints {
        body.push(format!("  {}", render_table_constraint(c, dialect)?));
    }

    let if_not_exists = if spec.if_not_exists { "IF NOT EXISTS " } else { "" };
    let mut stmts = vec![format!(
        "CREATE TABLE {}{} (\n{}\n){};",
        if_not_exists,
        table,
        body.join(",\n"),
        table_options_clause(&spec.options, dialect)?
    )];

    // Postgres carries comments as separate statements, not inline.
    stmts.extend(column_comment_stmts(&spec.table, &spec.columns, dialect)?);
    if dialect == Dialect::Postgres {
        if let Some(comment) = &spec.options.comment {
            stmts.push(format!(
                "COMMENT ON TABLE {} IS {};",
                table,
                quote_str_literal(comment)?
            ));
        }
    }

    for idx in &spec.indexes {
        stmts.push(compile_create_index(&CreateIndexSpec {
            table: spec.table.clone(),
            name: idx.name.clone(),
            columns: idx.columns.clone(),
            unique: idx.unique,
        }, dialect)?);
    }
    Ok(stmts)
}

pub fn compile_drop_table(spec: &DropTableSpec, dialect: Dialect) -> Result<String, String> {
    let table = dialect.quote_ident(&spec.table)?;
    let if_exists = if spec.if_exists { "IF EXISTS " } else { "" };
    // CASCADE is Postgres-only: SQLite errors on the keyword and MySQL ignores it.
    let cascade = if spec.cascade && dialect == Dialect::Postgres {
        " CASCADE"
    } else {
        ""
    };
    Ok(format!("DROP TABLE {}{}{};", if_exists, table, cascade))
}

/// `CREATE SCHEMA [IF NOT EXISTS] name` (Lucid/Knex `createSchema`). Postgres and
/// MySQL (where `SCHEMA` is a synonym for `DATABASE`); SQLite has no schemas.
pub fn compile_create_schema(spec: &CreateSchemaSpec, dialect: Dialect) -> Result<String, String> {
    if dialect == Dialect::Sqlite {
        return Err("E_UNSUPPORTED: SQLite has no schemas — createSchema needs Postgres or MySQL".into());
    }
    let name = dialect.quote_ident(&spec.name)?;
    let if_not_exists = if spec.if_not_exists { "IF NOT EXISTS " } else { "" };
    Ok(format!("CREATE SCHEMA {}{};", if_not_exists, name))
}

/// `DROP SCHEMA [IF EXISTS] name [CASCADE]` (Lucid/Knex `dropSchema`). CASCADE is
