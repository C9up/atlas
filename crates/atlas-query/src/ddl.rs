pub fn compile_drop_schema(spec: &DropSchemaSpec, dialect: Dialect) -> Result<String, String> {
    if dialect == Dialect::Sqlite {
        return Err("E_UNSUPPORTED: SQLite has no schemas — dropSchema needs Postgres or MySQL".into());
    }
    let name = dialect.quote_ident(&spec.name)?;
    let if_exists = if spec.if_exists { "IF EXISTS " } else { "" };
    let cascade = if spec.cascade && dialect == Dialect::Postgres {
        " CASCADE"
    } else {
        ""
    };
    Ok(format!("DROP SCHEMA {}{}{};", if_exists, name, cascade))
}

/// `CREATE TABLE new (LIKE old …)` (Lucid/Knex `createTableLike`) — copy a table's
/// structure. Postgres copies everything (`INCLUDING ALL`); MySQL uses `LIKE`;
/// SQLite has no `LIKE`, so it copies COLUMNS ONLY via `AS SELECT … WHERE 0`
/// (constraints/indexes are not carried — the same limitation Knex documents).
pub fn compile_create_table_like(
    spec: &CreateTableLikeSpec,
    dialect: Dialect,
) -> Result<String, String> {
    let table = dialect.quote_ident(&spec.table)?;
    let like = dialect.quote_ident(&spec.like_table)?;
    let if_not_exists = if spec.if_not_exists {
        "IF NOT EXISTS "
    } else {
        ""
    };
    let sql = match dialect {
        Dialect::Postgres => {
            format!("CREATE TABLE {if_not_exists}{table} (LIKE {like} INCLUDING ALL);")
        }
        Dialect::Mysql => format!("CREATE TABLE {if_not_exists}{table} LIKE {like};"),
        Dialect::Sqlite => {
            format!("CREATE TABLE {if_not_exists}{table} AS SELECT * FROM {like} WHERE 0;")
        }
    };
    Ok(sql)
}

pub fn compile_create_index(spec: &CreateIndexSpec, dialect: Dialect) -> Result<String, String> {
    let unique = if spec.unique { "UNIQUE " } else { "" };
    let name = dialect.quote_ident(&spec.name)?;
    let table = dialect.quote_ident(&spec.table)?;
    let cols: Result<Vec<String>, String> = spec.columns.iter().map(|c| dialect.quote_ident(c)).collect();
    Ok(format!("CREATE {}INDEX {} ON {} ({});", unique, name, table, cols?.join(", ")))
}

pub fn compile_drop_index(spec: &DropIndexSpec, dialect: Dialect) -> Result<String, String> {
    let name = dialect.quote_ident(&spec.name)?;
    let if_exists = if spec.if_exists { "IF EXISTS " } else { "" };
    Ok(format!("DROP INDEX {}{};", if_exists, name))
}

/// `CREATE [OR REPLACE] [MATERIALIZED] VIEW name [(cols)] AS <select>`.
///
/// The view NAME (and any column-list names) go through `quote_ident`, so they
/// are validated and quoted like every other identifier. The SELECT body is
/// raw, developer-authored migration SQL — the same trust level as `raw()` — so
/// it is embedded verbatim rather than parameterized.
#[derive(Debug, Clone, Serialize, Deserialize)]
#[serde(rename_all = "camelCase")]
pub struct CreateViewSpec {
    pub name: String,
    /// Raw SELECT body (without a trailing `;`).
    pub select: String,
    #[serde(default)]
    pub or_replace: bool,
    #[serde(default)]
    pub materialized: bool,
    #[serde(default)]
    pub columns: Option<Vec<String>>,
}

#[derive(Debug, Clone, Serialize, Deserialize)]
#[serde(rename_all = "camelCase")]
pub struct DropViewSpec {
    pub name: String,
    #[serde(default)]
    pub if_exists: bool,
    #[serde(default)]
    pub materialized: bool,
}

pub fn compile_create_view(spec: &CreateViewSpec, dialect: Dialect) -> Result<String, String> {
    // Trim a trailing terminator so the caller can pass a bare SELECT or a
    // `SELECT ...;` without producing `... ;;`.
    let select = spec.select.trim().trim_end_matches(';').trim_end();
    if select.is_empty() {
        return Err("E_EMPTY_VIEW_SELECT: createView requires a non-empty SELECT body".into());
    }
    // Materialized views exist only on Postgres. SQLite/MySQL have none.
    if spec.materialized && dialect != Dialect::Postgres {
        return Err("E_UNSUPPORTED: materialized views are Postgres-only".into());
    }
    // SQLite has no CREATE OR REPLACE VIEW (drop then create instead).
    if spec.or_replace && dialect == Dialect::Sqlite {
        return Err("E_UNSUPPORTED: SQLite does not support CREATE OR REPLACE VIEW".into());
    }
    // Postgres rejects OR REPLACE on a MATERIALIZED view.
    if spec.or_replace && spec.materialized {
        return Err(
            "E_UNSUPPORTED: CREATE OR REPLACE is not valid for materialized views".into(),
        );
    }
    let name = dialect.quote_ident(&spec.name)?;
    let or_replace = if spec.or_replace { "OR REPLACE " } else { "" };
    let materialized = if spec.materialized { "MATERIALIZED " } else { "" };
    let columns = match &spec.columns {
        Some(cols) if !cols.is_empty() => {
            let quoted: Result<Vec<String>, String> =
                cols.iter().map(|c| dialect.quote_ident(c)).collect();
            format!(" ({})", quoted?.join(", "))
        }
        _ => String::new(),
    };
    Ok(format!(
        "CREATE {}{}VIEW {}{} AS {};",
        or_replace, materialized, name, columns, select
    ))
}

pub fn compile_drop_view(spec: &DropViewSpec, dialect: Dialect) -> Result<String, String> {
    if spec.materialized && dialect != Dialect::Postgres {
        return Err("E_UNSUPPORTED: materialized views are Postgres-only".into());
    }
    let name = dialect.quote_ident(&spec.name)?;
    let if_exists = if spec.if_exists { "IF EXISTS " } else { "" };
    let materialized = if spec.materialized { "MATERIALIZED " } else { "" };
    Ok(format!("DROP {}VIEW {}{};", materialized, if_exists, name))
}

/// One column-level ALTER TABLE operation. Tagged union — TypeScript sends
/// `{ op: "addColumn", column: {...} }` etc.
#[derive(Debug, Clone, Serialize, Deserialize)]
#[serde(tag = "op", rename_all = "camelCase")]
pub enum AlterOp {
    AddColumn { column: ColumnDef },
    DropColumn { name: String },
    RenameColumn { from: String, to: String },
    /// Change a column's type / nullability. Postgres + MySQL only — SQLite
    /// cannot alter a column in place (the table must be recreated).
    ///
    /// `set_nullable` is tri-state on purpose: `None` leaves the existing
    /// nullability untouched, so a bare type change never silently adds or
    /// drops a NOT NULL constraint just because `ColumnDef.nullable` defaults
    /// to `false`. Only an explicit `.nullable()` / `.notNullable()` before
    /// `.alter()` sets it.
    // `rename_all` on the enum renames the VARIANTS, not their fields — a
    // multi-word field needs its own attribute or it silently stays snake_case
    // and `serde(default)` swallows the camelCase key TypeScript actually sends.
    #[serde(rename_all = "camelCase")]
    AlterColumn {
        column: ColumnDef,
        #[serde(default)]
        set_nullable: Option<bool>,
    },
    /// Toggle NOT NULL without restating the column type (Lucid/Knex
    /// `setNullable` / `dropNullable`). Postgres-only: MySQL's `MODIFY COLUMN`
    /// requires the full type, and SQLite cannot alter a column in place.
    SetNullable { name: String, nullable: bool },
    /// Add a table-level constraint. Rejected on SQLite, which cannot add a
    /// constraint after the fact — Knex refuses this too.
    AddConstraint { constraint: TableConstraint },
    /// Drop a named constraint (`dropChecks`, and the named form of the
    /// `dropUnique`/`dropForeign` family).
    DropConstraint { name: String },
    /// Drop the primary key (Lucid/Knex `dropPrimary`). MySQL spells it
    /// `DROP PRIMARY KEY`; Postgres drops the named constraint.
    DropPrimary { #[serde(default)] name: Option<String> },
    /// Drop a unique constraint (Lucid/Knex `dropUnique`). On MySQL a unique
    /// constraint is an index, so it drops by index name.
    DropUnique { name: String },
    /// Drop a foreign key (Lucid/Knex `dropForeign`). MySQL spells it
    /// `DROP FOREIGN KEY`; Postgres drops the constraint.
    DropForeign { name: String },
}

#[derive(Debug, Clone, Serialize, Deserialize)]
#[serde(rename_all = "camelCase")]
pub struct AlterTableSpec {
    pub table: String,
    pub operations: Vec<AlterOp>,
}

#[derive(Debug, Clone, Serialize, Deserialize)]
#[serde(rename_all = "camelCase")]
pub struct RenameTableSpec {
    pub table: String,
    pub to: String,
}

/// Compile ALTER TABLE → SQL statements, in call order. One operation can emit
/// more than one statement (Postgres splits a type change and a nullability
/// change into two `ALTER COLUMN` clauses), so results are flattened.
pub fn compile_alter_table(spec: &AlterTableSpec, dialect: Dialect) -> Result<Vec<String>, String> {
    if spec.operations.is_empty() {
        return Err("ALTER TABLE requires at least one operation".into());
    }
    let table = dialect.quote_ident(&spec.table)?;
    let per_op: Result<Vec<Vec<String>>, String> = spec.operations.iter().map(|op| match op {
        AlterOp::AddColumn { column } => {
            let mut stmts = vec![format!(
                "ALTER TABLE {} ADD COLUMN {}{};",
                table,
                render_column_def(column, dialect)?,
                render_position(column.position.as_ref(), dialect)?
            )];
            stmts.extend(column_comment_stmts(&spec.table, std::slice::from_ref(column), dialect)?);
            Ok(stmts)
        }
        AlterOp::DropColumn { name } => Ok(vec![format!(
            "ALTER TABLE {} DROP COLUMN {};",
            table,
            dialect.quote_ident(name)?
        )]),
        AlterOp::RenameColumn { from, to } => Ok(vec![format!(
            "ALTER TABLE {} RENAME COLUMN {} TO {};",
            table,
            dialect.quote_ident(from)?,
            dialect.quote_ident(to)?
        )]),
        AlterOp::AlterColumn { column, set_nullable } => match dialect {
            // Postgres: the physical type and the NOT NULL constraint are two
            // separate clauses. Nullability moves only when `set_nullable` says
            // so — see the `AlterOp::AlterColumn` docs for why it's tri-state.
            Dialect::Postgres => {
                let name = dialect.quote_ident(&column.name)?;
                let mut stmts = vec![format!(
                    "ALTER TABLE {} ALTER COLUMN {} TYPE {};",
                    table,
                    name,
                    dialect.map_column_type(&column.type_spec)?
                )];
                if let Some(nullable) = set_nullable {
                    stmts.push(nullability_stmt(&table, &name, *nullable));
                }
                Ok(stmts)
            }
            // MySQL rewrites the full column definition, so nullability rides
            // along inside `render_column_def` via `ColumnDef.nullable`. An
            // explicit `set_nullable` overrides it; otherwise the rendered
            // default (NOT NULL) would be a silent constraint change.
            Dialect::Mysql => {
                let mut col = column.clone();
                if let Some(nullable) = set_nullable {
                    col.nullable = *nullable;
                }
                Ok(vec![format!(
                    "ALTER TABLE {} MODIFY COLUMN {};",
                    table,
                    render_column_def(&col, dialect)?
                )])
            }
            Dialect::Sqlite => Err(
                "E_UNSUPPORTED: SQLite cannot ALTER COLUMN in place — recreate the table instead".into(),
            ),
        },
        AlterOp::AddConstraint { constraint } => match dialect {
            // Knex raises the same refusal: SQLite cannot add a constraint to
            // an existing table (the table has to be rebuilt).
            Dialect::Sqlite => Err(
                "E_UNSUPPORTED: SQLite cannot add a constraint to an existing table — declare it in createTable()".into(),
            ),
            _ => Ok(vec![format!(
                "ALTER TABLE {} ADD {};",
                table,
                render_table_constraint(constraint, dialect)?
            )]),
        },
        AlterOp::DropConstraint { name } => match dialect {
            Dialect::Sqlite => Err(
                "E_UNSUPPORTED: SQLite cannot drop a constraint — recreate the table instead".into(),
            ),
            _ => Ok(vec![format!(
                "ALTER TABLE {} DROP CONSTRAINT {};",
                table,
                dialect.quote_ident(name)?
            )]),
        },
        AlterOp::DropPrimary { name } => match dialect {
            Dialect::Mysql => Ok(vec![format!("ALTER TABLE {} DROP PRIMARY KEY;", table)]),
            Dialect::Postgres => {
                // Postgres drops the constraint by name; its default is `<table>_pkey`.
                let constraint = name.clone().unwrap_or_else(|| format!("{}_pkey", spec.table));
                Ok(vec![format!(
                    "ALTER TABLE {} DROP CONSTRAINT {};",
                    table,
                    dialect.quote_ident(&constraint)?
                )])
            }
            Dialect::Sqlite => Err(
                "E_UNSUPPORTED: SQLite cannot drop a primary key — recreate the table instead".into(),
            ),
        },
        AlterOp::DropUnique { name } => match dialect {
            // On MySQL a unique constraint IS an index and drops as one.
            Dialect::Mysql => Ok(vec![format!(
                "ALTER TABLE {} DROP INDEX {};",
                table,
                dialect.quote_ident(name)?
            )]),
            Dialect::Postgres => Ok(vec![format!(
                "ALTER TABLE {} DROP CONSTRAINT {};",
                table,
                dialect.quote_ident(name)?
            )]),
            Dialect::Sqlite => Err(
                "E_UNSUPPORTED: SQLite cannot drop a unique constraint — drop the index with schema.dropIndex() if you created one".into(),
            ),
        },
        AlterOp::DropForeign { name } => match dialect {
            Dialect::Mysql => Ok(vec![format!(
                "ALTER TABLE {} DROP FOREIGN KEY {};",
                table,
                dialect.quote_ident(name)?
            )]),
            Dialect::Postgres => Ok(vec![format!(
                "ALTER TABLE {} DROP CONSTRAINT {};",
                table,
                dialect.quote_ident(name)?
            )]),
            Dialect::Sqlite => Err(
                "E_UNSUPPORTED: SQLite cannot drop a foreign key — recreate the table instead".into(),
            ),
        },
        AlterOp::SetNullable { name, nullable } => match dialect {
            Dialect::Postgres => Ok(vec![nullability_stmt(
                &table,
                &dialect.quote_ident(name)?,
                *nullable,
            )]),
            Dialect::Mysql => Err(format!(
                "E_UNSUPPORTED: MySQL cannot toggle NOT NULL without restating the column type — use `table.<type>('{}').{}().alter()` instead",
                name,
                if *nullable { "nullable" } else { "notNullable" }
            )),
            Dialect::Sqlite => Err(
                "E_UNSUPPORTED: SQLite cannot ALTER COLUMN in place — recreate the table instead".into(),
            ),
        },
    }).collect();
    Ok(per_op?.into_iter().flatten().collect())
}

/// MySQL `FIRST` / `AFTER col` placement clause for an added column. Postgres
/// and SQLite always append, so asking for a position there is an error rather
/// than a silently ignored instruction.
fn render_position(
    position: Option<&ColumnPosition>,
    dialect: Dialect,
) -> Result<String, String> {
    let Some(position) = position else { return Ok(String::new()) };
    if dialect != Dialect::Mysql {
        return Err(
            "E_UNSUPPORTED: first()/after() is MySQL-only — Postgres and SQLite always append a new column".into(),
        );
    }
    Ok(match position {
        ColumnPosition::First => " FIRST".to_string(),
        ColumnPosition::After { column } => format!(" AFTER {}", dialect.quote_ident(column)?),
    })
}

/// Postgres `SET`/`DROP NOT NULL` clause. Both identifiers must already be quoted.
fn nullability_stmt(table: &str, column: &str, nullable: bool) -> String {
    format!(
        "ALTER TABLE {} ALTER COLUMN {} {} NOT NULL;",
        table,
        column,
        if nullable { "DROP" } else { "SET" }
    )
}

/// Compile RENAME TABLE — `ALTER TABLE old RENAME TO new` (portable across all three dialects).
pub fn compile_rename_table(spec: &RenameTableSpec, dialect: Dialect) -> Result<String, String> {
    Ok(format!(
        "ALTER TABLE {} RENAME TO {};",
        dialect.quote_ident(&spec.table)?,
        dialect.quote_ident(&spec.to)?
    ))
}

